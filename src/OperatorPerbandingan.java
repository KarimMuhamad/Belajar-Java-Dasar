public class OperatorPerbandingan {
    public static void main(String[] args) {
        int a = 5;
        int b = 12;

        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println("=================");

        // Logic Operator
        System.out.println(true && true);
        System.out.println(true && false);
        System.out.println(true || false);
        System.out.println(false|| false);
    }
}
